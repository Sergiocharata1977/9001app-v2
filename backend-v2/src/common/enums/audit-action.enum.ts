/**
 * Niveles de criticidad para las acciones de auditoría
 */
export enum AuditLevel {
  LOW = 'LOW',           // Operaciones de lectura, navegación
  MEDIUM = 'MEDIUM',     // Operaciones de escritura normales
  HIGH = 'HIGH',         // Operaciones críticas del negocio
  CRITICAL = 'CRITICAL', // Operaciones de seguridad y configuración
}

