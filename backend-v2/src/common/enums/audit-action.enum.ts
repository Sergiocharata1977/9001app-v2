/**
 * Obtiene el nivel de criticidad de una acción de auditoría
 * @param action Acción de auditoría
 * @returns Nivel de criticidad
 */
export function getAuditLevel(action: AuditAction): AuditLevel {
  return AUDIT_ACTION_LEVELS[action] || AuditLevel.MEDIUM;
}

/**
 * Verifica si una acción requiere retención a largo plazo
 * @param action Acción de auditoría
 * @returns true si requiere retención a largo plazo
 */
export function requiresLongTermRetention(action: AuditAction): boolean {
  const level = getAuditLevel(action);
  return level === AuditLevel.CRITICAL || level === AuditLevel.HIGH;
}